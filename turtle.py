import turtle
t=turtle.Turtle()
t.shape("turtle")
t.width(5)

t.color("yellow")
t.circle(60)

t.up()
t.goto(130, 0)
t.down()
t.color("green")
t.circle(60)

t.up()
t.goto(200, 70)
t.down()
t.color("red")
t.circle(60)

t.up()
t.goto(70, 70)
t.down()
t.color("black")
t.circle(60)

t.up()
t.goto(-60, 70)
t.down()
t.color("blue")
t.circle(60)
